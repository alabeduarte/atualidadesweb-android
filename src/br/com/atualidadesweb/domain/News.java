package br.com.atualidadesweb.domain;

public class News {
    private String url;
    private String title;
    private String subtitle;
    private String image;

    public String getUrl() {
        return url;
    }

    public String getTitle() {
        return title;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public String getImage() {
        return image;
    }
}
